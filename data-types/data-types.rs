fn main() {
    let b:i32 = -1;    
    println!("Integer i32 {}", b);
    let a:u32 = 1;    
    println!("Data types u32 {}", a);
    let c:u8 = 252;    
    println!("Data types u8 {}", c);

    let floating_point:f32 = 252.3;    
    println!("Data types f32 {}", floating_point);

    let floating_point2:f64 = 252.357;
    println!("Data types f32 {}", floating_point2);

    let integer_u64:u64 = 252;
    println!("Data types u64 {}", integer_u64);

    let arch_int:isize = 252;
    println!("Data types isize {}", arch_int);

    let bolean:bool = false;    
    println!("Data types bool {}", bolean);
}

